# escape with tab function
tabby_cat = "\tI'm tabbed in."
#escape with line break
persian_cat = "I'm split\non a line."
#escape backslash
backslash_cat = "I'm \\ a \\ cat."
#makes list of with """ """ which keeps line spacing the same
fat_cat = """
I'll do a list:
\t- Cat Food
\t- Fishies
\t- Catnip\n\t* Grass
"""
#prints each of the variables above
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
